    <h1 class="historia-instituto">Nuestra Historia</h1> 
    
    <h3 class="historia-instituto"> Instituto de idiomas</h3>

    <p> A comienzos de año 2021, los profesores Valeria Palacios, Enrrique Espinoza y Jesica Queupumil, decidieron dar comienzo a este proyecto motivados a 
      satisfacer la demanda existente en base al aprendizaje de diferentes idiomas
    
    Ellos a cargo de la institución y de las áreas de inglés y francés, fueron incorporando otras docentes que cubrían
 las materias de alemán e italiano. Gracias al compromiso y responsabilidad en el trabajo, 
el instituto fue creciendo a pasos agigantados
 <br>
 <br>
    Nuestro instituto cuenta con un prestigio y una trayectoria académica que avala el éxito de la misma 
 gracias a la confianza y al apoyo de la comunidad educativa.
 <br>
 <br>
    Contamos con un cuerpo docente altamente capacitado que se adapta constantemente 
 a las exigencias y demandas actuales del alumno y la sociedad.
